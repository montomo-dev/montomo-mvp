export const RARE_ENCOUNTER_RATE = 0.05;

export function rollWildSpecies(pool = WILD_SPECIES, random = Math.random) {
  if (random() < RARE_ENCOUNTER_RATE) return RARE_SPECIES;
  return pool[Math.floor(random() * pool.length)];
}

let uidCounter = 1;

export function peekUidCounter() {
  return uidCounter;
}

export function ensureUidAbove(maxUid) {
  if (maxUid + 1 > uidCounter) uidCounter = maxUid + 1;
}

export function createMonster(speciesId, level) {
  const species = SPECIES[speciesId];
  const clampedLevel = Math.min(MAX_LEVEL, Math.max(1, level));
  const stats = statsFor(species, clampedLevel);
  return {
    uid: uidCounter++,
    speciesId,
    name: species.name,
    level: clampedLevel,
    exp: 0,
    maxHp: stats.maxHp,
    hp: stats.maxHp,
    atk: stats.atk,
    def: stats.def,
    spd: stats.spd,
    skills: species.learnset.filter((e) => e.level <= clampedLevel).map((e) => e.skill),
  };
}

// なかまのnameフィールドは生成時の日本語名を保持しているため、英語表示時は
// 現在のspeciesIdから改めてnameEnを引く(進化後も正しい名前になる)
export function monsterName(monster, lang = "ja") {
  if (lang === "en") return SPECIES[monster.speciesId]?.nameEn || monster.name;
  return monster.name;
}
